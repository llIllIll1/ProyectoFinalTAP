    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int ID = Integer.parseInt(txtID.getText());
        String  Nombre = txtNombre.getText();
        String  Volumen = txtVolumen.getText();
        String  Editorial = txtEditorial.getText();
        String Genero;
        if(radAccion.isSelected() == true)
        {
            Genero = "A";
        }
        else if(radShonen.isSelected() == true)
        {
            Genero = "S";
        }
        else
        {
            Genero = "A";
        }
        
        try
        {
            Connection con  = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement
        ("UPDATE Productos SET Nombre=?,Volumen=?,Genero=?,Editorial=? WHERE ID =?");
            ps.setString(1, Nombre);
            ps.setString(2, Volumen);
            ps.setString(3, Genero);
            ps.setString(4, Editorial);
            ps.setInt(5, ID);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro modificado");
            Limpiar();
            cargarTabla();
        }
        
        catch(SQLException e)
                {
                    JOptionPane.showMessageDialog(null, e.toString());
                } 
    }//GEN-LAST:event_btnModificarActionPerformed

