    private void TablaPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaPedidosMouseClicked
        try
        {
            PreparedStatement ps;
            ResultSet rs;

            int fila = TablaPedidos.getSelectedRow();
            int ID = Integer.parseInt(TablaPedidos.getValueAt(fila,0).toString());
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement
            ("SELECT Nombre,Volumen,Genero,Editorial FROM Pedidos WHERE  id=?");
            ps.setInt(1, ID);
            rs = ps.executeQuery();
            while(rs.next())
            {
                txtID.setText(String.valueOf(ID));
                txtNombre.setText(rs.getString("Nombre"));
                txtVolumen.setText(rs.getString("Volumen"));
                txtEditorial.setText(rs.getString("Editorial"));
                if(rs.getString("Genero").equals("A"))
                {
                    radAccion.setSelected(true);
                } else if(rs.getString("Genero").equals("S"))
                {
                    radShonen.setSelected(true);
                }
            }
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_TablaPedidosMouseClicked


