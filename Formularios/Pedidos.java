    private void cargarTabla()
    {
        DefaultTableModel modeloTabla = (DefaultTableModel) TablaPedidos.getModel(); 
        modeloTabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        int[] anchos = {10,30,10,30,40};
        for ( int i = 0; i < TablaPedidos.getColumnCount() ; i++)
        {
            TablaPedidos.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        
        try
        {
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement
        ("SELECT ID,Nombre,Volumen,Genero,Editorial FROM Pedidos");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while(rs.next())
            {
                Object[] fila = new Object[columnas];
                for(int indice = 0; indice < columnas ; indice++)
                {
                    fila[indice] = rs.getObject(indice + 1);
                }
                modeloTabla.addRow(fila);
            }
        }catch(SQLException e)
        {

         JOptionPane.showMessageDialog(null, e.toString());
            
        }
    }

