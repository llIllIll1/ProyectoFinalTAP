    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String  Nombre = txtNombre.getText();
        String  Volumen = txtVolumen.getText();
        String  Editorial = txtEditorial.getText();
        String Genero;
        if(radAccion.isSelected() == true)
        {
            Genero = "A";
        }
        else if(radShonen.isSelected() == true)
        {
            Genero = "S";
        }
        else if(radRomance.isSelected() == true)
        {
            Genero = "R";
        }
        else if(radHorror.isSelected() == true)
        {
            Genero = "H";
        }else
        {
            Genero = "A";
        }
        
        try
        {
            Connection con  = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement
        ("INSERT INTO Pedidos (Nombre,Volumen,Genero,Editorial,Activo) VALUES (?,?,?,?,?)");
            ps.setString(1, Nombre);
            ps.setString(2, Volumen);
            ps.setString(3, Genero);
            ps.setString(4, Editorial);
            ps.setInt(5, 1);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro guardado");
            Limpiar();
            cargarTabla();
        }
        
        catch(SQLException e)
                {
                    JOptionPane.showMessageDialog(null, e.toString());
                }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

