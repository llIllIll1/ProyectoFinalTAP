    private void cargarTabla()
    {
        DefaultTableModel modeloTabla = (DefaultTableModel) TablaUsuario.getModel(); 
        modeloTabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        int[] anchos = {10,30,30,10,10,50 };
        for ( int i = 0; i < TablaUsuario.getColumnCount() ; i++)
        {
            TablaUsuario.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            
        }
        
        try
        {
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement
        ("SELECT ID,RFC,Nombre,Telefono,Sexo,Correo FROM Usuarios");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while(rs.next())
            {
                Object[] fila = new Object[columnas];
                for(int indice = 0; indice < columnas ; indice++)
                {
                    fila[indice] = rs.getObject(indice + 1);
                }
                modeloTabla.addRow(fila);
            }
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
            
        }
    }
