    private void TablaUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaUsuarioMouseClicked
        // TODO add your handling code here
        try
        {
            PreparedStatement ps;
            ResultSet rs;
            
            int fila = TablaUsuario.getSelectedRow();
            int ID = Integer.parseInt(TablaUsuario.getValueAt(fila,0).toString()); 
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement
        ("SELECT RFC,Nombre,Telefono,Sexo,Correo FROM Usuarios WHERE  id=?");
            ps.setInt(1, ID);
            rs = ps.executeQuery();
            while(rs.next())
            {
                txtID.setText(String.valueOf(ID));
                txtRFC.setText(rs.getString("RFC"));
                txtNombre.setText(rs.getString("Nombre"));
                txtTelefono.setText(rs.getString("Telefono"));
                txtCorreoElectronico.setText(rs.getString("Correo"));
                if(rs.getString("Sexo").equals("M"))
                {
                    radMasculino.setSelected(true);
                } else if(rs.getString("Sexo").equals("F"))
                {
                    radFemenino.setSelected(true);
                }
            }
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_TablaUsuarioMouseClicked


